    public void setData(ArrayList<MCArticuloVenta> mcav, float precio, float precioIVA, float pago, float cambio, 
            String usuario, String folio ){
        for(int i=0; i<mcav.size(); i++){
            MCArticuloVenta mv = mcav.get(i);
            modelo.addRow(new Object[]{mv.getId(), mv.getNombre(), mv.getPrecio(), mv.getPrecioIVA(), mv.getCantidad()});
        }
        DecimalFormat formato = new DecimalFormat(".00");
        this.sPago.setText("$ "+formato.format(pago));
        this.sTotal.setText("$ "+formato.format(precio));
        this.sTotalIva.setText("$ "+formato.format(precioIVA));
        this.sCambio.setText("$ "+formato.format(cambio));
        this.sAtendio.setText(usuario);
        this.sFolio.setText("Ticket #"+folio);
        DateFormat df = DateFormat.getDateInstance(DateFormat.FULL);
        this.sFecha.setText(df.format(new Date()));
       
    }

