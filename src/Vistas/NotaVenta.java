    private void bPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bPDFActionPerformed
        /*    
         */
        this.bPDF.setVisible(false);
        this.bImprimir.setVisible(false);
        try {
            Document document = new Document(PageSize.A4.rotate(), 100, 100, 100, 100);
            PdfWriter writer = PdfWriter.getInstance(document, new FileOutputStream("c:\\CODICE_DATA\\"+sFolio.getText()+".pdf"));
            document.open();
            PdfContentByte cb = writer.getDirectContent();
            PdfTemplate tp = cb.createTemplate(contenido.getWidth(), contenido.getHeight());
            Graphics2D g2 = tp.createGraphics(contenido.getWidth(), contenido.getHeight());
            contenido.print(g2);
            g2.dispose();
            cb.addTemplate( tp, 60, 60);
            document.close();
            JOptionPane.showMessageDialog(this, "PDF Creado en c:\\CODICE_DATA\\"+sFolio.getText()+".pdf");
        } catch (DocumentException ex) {
            JOptionPane.showMessageDialog(this, "Error al crear el documento");
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Archivo no encontrado");
        }
        //d.close();
        this.bPDF.setVisible(true);
        this.bImprimir.setVisible(true);

    }//GEN-LAST:event_bPDFActionPerformed

    private void bImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bImprimirActionPerformed
        // TODO add your handling code here:
        this.bPDF.setVisible(false);
        this.bImprimir.setVisible(false);
        PrinterJob pjob = PrinterJob.getPrinterJob();
        PageFormat preformat = pjob.defaultPage();
        preformat.setOrientation(PageFormat.PORTRAIT);

        PageFormat postformat = pjob.pageDialog(preformat);
//If user does not hit cancel then print.
        if (preformat != postformat) {
            //Set print component
            pjob.setPrintable(new Impresor(mthis), postformat);
            if (pjob.printDialog()) {
                try {
                    pjob.print();
                } catch (PrinterException ex) {
                }
            }
        }
        this.bPDF.setVisible(true);
        this.bImprimir.setVisible(true);
    }//GEN-LAST:event_bImprimirActionPerformed

