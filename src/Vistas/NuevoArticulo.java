    private void consulta() {
        tipos = new ArrayList<MCTipo>();
        try {
            this.sTipo.removeAllItems();
            ResultSet rs = new Conexion().getSt().executeQuery("select * from Tipo");
            while (rs.next()) {
                this.sTipo.addItem(rs.getObject(2));
                this.tipos.add(new MCTipo(rs.getInt(1), rs.getString(2)));
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }
