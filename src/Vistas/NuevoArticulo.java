    private void bAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregarActionPerformed

        String mensaje = "";
        String validarIDS = ValidadorEntradas.validarIDS(this.sClave.getText());
        String validarTexto = ValidadorEntradas.validarTexto(this.sModelo.getText(), "Modelo");
        String validarCosto = ValidadorEntradas.validarCosto(this.sCosto.getText());
        String validarPrecio = ValidadorEntradas.validarPrecio(this.sPrecio.getText());
        
        if(!validarIDS.equals("OK"))
            JOptionPane.showMessageDialog(this, validarIDS);   
        else if(!validarTexto.equals("OK")){
            JOptionPane.showMessageDialog(this, validarTexto);
        } else if(!validarCosto.equals("OK")){
            JOptionPane.showMessageDialog(this, validarCosto);
        }else if(!validarPrecio.equals("OK")){
            JOptionPane.showMessageDialog(this, validarPrecio);
        }else{

            try {
                // TODO add your handling code here:
                new Conexion().getSt().execute("insert into articulo values("
                        + "" + Integer.parseInt(this.sClave.getText()) + ", "
                        + "'" + this.sModelo.getText().toUpperCase() + "', "
                        + "" + Float.parseFloat(this.sCosto.getText()) + ", "
                        + "" + Float.parseFloat(this.sPrecio.getText()) + ","
                        + "" + this.tipos.get(this.sTipo.getSelectedIndex()).getId() + ""
                        + ")");
                JOptionPane.showMessageDialog(null, "Carga exitosa", "", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
        

        Inventario.mthis.actualizarDatos();
        dispose();
    }//GEN-LAST:event_bAgregarActionPerformed

