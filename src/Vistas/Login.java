    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean encontrado = false;
        
        String usuariotxt = this.jTextField1.getText().toString();
        String contrasenatxt = this.jPasswordField1.getText().toString();
        String validarUsuario = ValidadorEntradas.validarUsuario(usuariotxt);
        String validarContrasena = ValidadorEntradas.validarContrasena(contrasenatxt);
        
        System.out.println(validarUsuario);
        System.out.println(validarContrasena);
        
        if(!validarUsuario.equals("OK"))
            JOptionPane.showMessageDialog(this, validarUsuario.toUpperCase());
        if(!validarContrasena.equals("OK"))
            JOptionPane.showMessageDialog(this, validarContrasena.toUpperCase());
        if(validarUsuario.equals("OK") && validarContrasena.equals("OK")){
            try {
                // new Home().setUsuario(this.jTextField1.getText());
                // this.dispose();
                ResultSet rs = new Conexion().getSt().executeQuery("select * from usuario where usuario = '"+usuariotxt+"' and contrasena = '"+contrasenatxt+"';");
                while(rs.next()){
                    System.out.println(rs.getString(3));
                    System.out.println(rs.getString(4));
                    if(usuariotxt.equals(rs.getString(3)) && contrasenatxt.equals(rs.getString(4))){
                        new Home().setUsuario(this.jTextField1.getText());
                        encontrado = true;
                        dispose();
                    }
                        
                }
            } catch (SQLException ex) {
                
            }
            
        }else{
            
        
        }
        this.jTextField1.setText(null);
        this.jPasswordField1.setText(null);
        if(!encontrado)
            JOptionPane.showMessageDialog(this, "Credenciales invalidas");
        
    }//GEN-LAST:event_jButton1ActionPerformed

